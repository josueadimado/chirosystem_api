RECEPTIONIST_PERSONA = """\
You are Sarah, the front-desk receptionist at Relief Chiropractic. You love helping people feel \
welcome and you take real pride in getting their care scheduled smoothly. You're on a phone call \
with a patient or future patient.

TONE — JOVIAL YET PROFESSIONAL:
- Sound genuinely glad they called: bright, upbeat, and human — like a great front desk who \
enjoys their job. A touch of warmth and light enthusiasm is perfect.
- Stay professional for healthcare: respectful, clear, never flippant about health, pain, or \
scheduling. No slang that sounds teenage; no jokes at the patient's expense.
- Use natural contractions (you're, we'll, that's). Sprinkle in friendly phrases like \
"Wonderful!", "I'd be happy to help with that", "That's great", "Love it — let's get you booked", \
"Perfect timing" — but rotate; don't repeat the same opener every time.
- Vary your wording every reply so it never feels robotic or scripted.
- One short beat of rapport is fine (e.g. glad they chose the clinic, hope their day's going well) \
— then move the booking forward.
- Keep each reply SHORT: 1–3 sentences. Phone callers want quick, clear answers.

NEVER:
- Say you're an AI, bot, or automated system — you are Sarah at the desk.
- Invent services, prices, times, or policies not given in the instructions.

ALWAYS:
- Include every REQUIRED FACT from the instructions — names, dates, times, services, prices.
- When you repeat a date and time, use the plain-English form given in the instructions (weekday, month with \
ordinal day like "April 8th", and times like "2:30 P.M.") — not ISO dates or 24-hour time.
- If the caller is a returning patient (you are told this in the instructions), do NOT say "new patient," \
"brand-new appointment," or anything that sounds like their first visit ever. Say "your visit," \
"get you scheduled," "book that for you," or "another appointment" instead.
"""


async def _llm_respond(instruction: str, fallback: str) -> str:
    """Call GPT-4o-mini to generate a natural response. Falls back to static text."""
    api_key = getattr(settings, "OPENAI_API_KEY", "") or ""
    if not api_key.strip():
        return fallback

    model = getattr(settings, "OPENAI_VOICE_MODEL", None) or "gpt-5.4-nano"

    try:
        client = await _get_http_client()
        resp = await client.post(
            "https://api.openai.com/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model,
                "temperature": 0.82,
                "max_tokens": 120,
                "messages": [
                    {"role": "system", "content": RECEPTIONIST_PERSONA},
                    {"role": "user", "content": instruction},
                ],
            },
        )
        resp.raise_for_status()
        data = resp.json()
        text = data["choices"][0]["message"]["content"].strip()
        text = text.strip('"').strip("'")
        return text if text else fallback
    except Exception:
        logger.exception("LLM response generation failed, using fallback")
        return fallback


async def _stream_openai_tokens_to_ws(ws: WebSocket, instruction: str, fallback: str) -> str:
    """
    Stream chat completion deltas to Twilio as they arrive (last=false), then one final last=true.
    Twilio recommends this over a single blob for lower latency to first audio.
    """
    api_key = (getattr(settings, "OPENAI_API_KEY", "") or "").strip()
    if not api_key:
        await _send_text(ws, fallback)
        return fallback

    model = getattr(settings, "OPENAI_VOICE_MODEL", None) or "gpt-5.4-nano"
    queued: str | None = None
    full_parts: list[str] = []

    try:
        client = await _get_http_client()
        async with client.stream(
            "POST",
            "https://api.openai.com/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model,
                "temperature": 0.82,
                "max_tokens": 120,
                "stream": True,
                "messages": [
                    {"role": "system", "content": RECEPTIONIST_PERSONA},
                    {"role": "user", "content": instruction},
                ],
            },
            timeout=httpx.Timeout(45.0, connect=10.0),
        ) as resp:
            resp.raise_for_status()
            async for line in resp.aiter_lines():
                if not line.startswith("data: "):
                    continue
                data = line[6:].strip()
                if data == "[DONE]":
                    break
                try:
                    obj = json.loads(data)
                except json.JSONDecodeError:
                    continue
                choices = obj.get("choices") or []
                if not choices:
                    continue
                delta = (choices[0].get("delta") or {})
                content = delta.get("content") or ""
                if not content:
                    continue
                full_parts.append(content)
                if queued is not None:
                    await ws.send_json({"type": "text", "token": queued, "last": False})
                queued = content

        if queued is not None:
            await ws.send_json({"type": "text", "token": queued, "last": True})
        full = "".join(full_parts).strip().strip('"').strip("'")
        if not full:
            await _send_text(ws, fallback)
            return fallback
        _last_responses[id(ws)] = full
        return full
    except Exception:
        logger.exception("OpenAI streaming failed, using fallback")
        await _send_text(ws, fallback)
        return fallback


async def _speak_llm(ws: WebSocket, instruction: str, fallback: str) -> str:
    """Generate Sarah's line: stream to Twilio when VOICE_LLM_STREAM is on, else one round-trip."""
    if getattr(settings, "VOICE_LLM_STREAM", True) and (getattr(settings, "OPENAI_API_KEY", "") or "").strip():
        return await _stream_openai_tokens_to_ws(ws, instruction, fallback)
    text = await _llm_respond(instruction, fallback)
    await _send_text(ws, text)
    return text


async def _end_session_speaking_llm(ws: WebSocket, instruction: str, fallback: str):
    await _speak_llm(ws, instruction, fallback)
    await ws.send_json({"type": "end"})


