async def do_book_all(ws: WebSocket, state: ConversationState):
    # Fresh booking run — drop any add-on offer payload from a prior pass.
    state.pending_final_booked = None

    phone = state.from_number
    if phone.startswith("tel:"):
        phone = phone[4:]

    # (service_name, plain-English date+time phrase)
    booked: list[tuple[str, str]] = []
    last_end_time = None
    last_date = None
    today = _today_clinic_date()

    for idx in range(state.current_svc_idx, len(state.services)):
        svc = state.services[idx]
        state.current_svc_idx = idx

        if svc.booked_appt is not None:
            appt = svc.booked_appt
            when_plain = _booking_datetime_plain_english(
                appt.appointment_date, appt.start_time, today=today
            )
            if not any(b[0] == svc.service_name for b in booked):
                booked.append((svc.service_name, when_plain))
            last_date = appt.appointment_date.isoformat()
            last_end_time = _add_minutes(appt.start_time, svc.service_duration)
            continue

        if idx > 0 and not svc.appointment_date and last_date and last_end_time:
            svc.appointment_date = last_date
            prev_ent = state.services[idx - 1]
            gap_m = _inter_visit_buffer_minutes(prev_ent, svc)
            buffer_start = _add_minutes(last_end_time, gap_m)
            svc.start_time = buffer_start.strftime("%I:%M %p")

        result = await _book_single(ws, state, svc, phone)
        if result is None:
            # Partial failure: some services already booked
            if booked:
                summary = booked[0][1]
                await _speak_llm(
                    ws,
                    f"The first appointment was booked successfully ({booked[0][0]} on {summary}), but "
                    f"the next one ({svc.service_name}) couldn't be booked at that time. "
                    f"Let the caller know the first is confirmed, and ask for a new "
                    f"date/time for the remaining service. Be reassuring. "
                    f"REQUIRED FACTS: first visit is confirmed for {summary}.",
                    f"Good news — your {booked[0][0]} is confirmed for {summary}! "
                    f"But the slot for {svc.service_name} isn't available. "
                    f"What other time would work for that one?",
                )
            return

        appt, when_plain = result
        svc.booked_appt = appt
        booked.append((svc.service_name, when_plain))

        last_date = svc.appointment_date
        t = _parse_time_12h(svc.start_time)
        if t:
            last_end_time = _add_minutes(t, svc.service_duration)

    if (
        len(booked) == 1
        and len(state.services) == 1
        and _catalog_has_both_chiropractic_and_massage(state.catalog)
    ):
        first = state.services[0]
        if first.service_type in ("chiropractic", "massage"):
            other_phrase = (
                "a massage visit"
                if first.service_type == "chiropractic"
                else "a chiropractic visit"
            )
            other_plain = (
                "massage therapy"
                if first.service_type == "chiropractic"
                else "chiropractic care"
            )
            state.pending_final_booked = booked
            state.step = "addon_offer"
            state.retries = 0
            when0 = booked[0][1]
            await _speak_llm(
                ws,
                f"One visit was just booked successfully: {first.service_name} on {when0}. "
                f"The clinic also offers {other_plain} besides what they chose today. "
                f"FIRST ask in a warm, natural way if that was everything they needed (e.g. was that all for today). "
                f"THEN mention that {other_phrase} is available here too if they'd ever like to try it — "
                f"totally optional, no pressure, not salesy. If they want it, help them book; if they're done, wrap up kindly. "
                f"REQUIRED FACTS: confirmed booking is {first.service_name} on {when0}. Other service category: {other_plain}.",
                f"Wonderful — you're all set for {first.service_name} on {when0}. "
                f"Was that everything you needed today? "
                f"We also offer {other_plain} if you'd like to add {other_phrase} while we're on the line — "
                f"happy to, or I'm glad to let you go. What works for you?",
            )
            return

    await _build_final_message(ws, state, booked)


def _book_single_try_sync(
    payload: dict,
    *,
    call_sid: str,
    from_number: str,
    transcript: str,
) -> tuple[str, object | None, str | None]:
    """
    Run serializer + booking in one sync DB transaction context.

    Returns (status, appointment_or_none, error_detail_or_none).
    status is 'ok' | 'serializer' | 'intake' | 'slot'.
    """
    ser = PublicBookingSerializer(data=payload)
    if not ser.is_valid():
        logger.info("Voice serializer errors: %s", ser.errors)
        upsert_voice_call_log(
            call_sid=call_sid,
            from_number=from_number,
            transcript=transcript,
            outcome=VoiceCallLog.Outcome.SERIALIZER_REJECTED,
            detail=str(ser.errors)[:2000],
        )
        return "serializer", None, None
    vd = ser.validated_data
    appt, err = create_appointment_from_public_booking(vd)
    if err:
        logger.info("Voice booking error: %s", err)
        upsert_voice_call_log(
            call_sid=call_sid,
            from_number=from_number,
            transcript=transcript,
            outcome=VoiceCallLog.Outcome.SLOT_OR_RULE_ERROR,
            detail=err[:2000],
        )
        p_phone = normalize_phone(vd["phone"])
        patient = Patient.objects.filter(phone=p_phone).first()
        try:
            service = Service.objects.get(pk=vd["service_id"])
        except Service.DoesNotExist:
            patient = None
            service = None
        if (
            patient
            and service
            and service.service_type == Service.ServiceType.CHIROPRACTIC
            and not service.is_new_client_intake
        ):
            must = chiropractic_booking_must_use_intake(patient, service)
            if must and must == err:
                return "intake", None, err
        return "slot", None, err
    upsert_voice_call_log(
        call_sid=call_sid,
        from_number=from_number,
        transcript=transcript,
        outcome=VoiceCallLog.Outcome.BOOKED,
        appointment=appt,
    )
    return "ok", appt, None


_book_single_try_async = sync_to_async(_book_single_try_sync, thread_sensitive=True)


