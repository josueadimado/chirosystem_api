@app.websocket("/ws/voice")
async def voice_websocket(ws: WebSocket):
    await ws.accept()
    state: ConversationState | None = None

    try:
        while True:
            raw = await ws.receive_text()
            msg = json.loads(raw)
            msg_type = msg.get("type", "")

            if msg_type == "setup":
                call_sid = msg.get("callSid", "")
                from_number = msg.get("from", "")
                state = ConversationState(call_sid, from_number)

                await async_upsert_voice_call_log(
                    call_sid=call_sid,
                    from_number=from_number,
                    outcome=VoiceCallLog.Outcome.PROMPTED,
                )
                logger.info("Voice WS [%s] setup from %s", call_sid[:8], from_number)

                # Returning patient: TwiML greeting already welcomed them
                # and asked about services — just set up state silently
                returning = await _returning_setup_async(from_number)
                if returning:
                    state.first_name = returning["first_name"]
                    state.last_name = returning["last_name"]
                    state.step = "service"
                    state.is_returning = True
                    state.catalog = returning["catalog"]
                    if returning["last_service_id"]:
                        state.last_service_id = returning["last_service_id"]
                    logger.info(
                        "Voice WS [%s] returning patient: %s %s",
                        call_sid[:8], returning["first_name"], returning["last_name"],
                    )

            elif msg_type == "prompt":
                speech = msg.get("voicePrompt", "").strip()

                if not state:
                    logger.warning("Received prompt before setup, ignoring")
                    continue

                logger.info(
                    "Voice WS [%s] step=%s speech=%r",
                    state.call_sid[:8], state.step, speech[:120] if speech else "",
                )

                await async_upsert_voice_call_log(
                    call_sid=state.call_sid,
                    from_number=state.from_number,
                    transcript=speech,
                )

                if not speech:
                    state.retries += 1
                    if state.retries >= MAX_RETRIES:
                        await async_upsert_voice_call_log(
                            call_sid=state.call_sid,
                            from_number=state.from_number,
                            outcome=VoiceCallLog.Outcome.EMPTY_SPEECH,
                            detail="silence",
                        )
                        await _end_session_speaking_llm(
                            ws,
                            "The caller has been completely silent for a while. "
                            "Say goodbye gently — maybe they got disconnected. "
                            "Invite them to call back anytime.",
                            "Looks like we may have lost the connection. "
                            "Feel free to call back anytime. Take care!",
                        )
                        break

                    context = _NUDGE_CONTEXTS.get(state.step, "The caller is silent. Gently ask if they're still there.")
                    fallback = _NUDGE_FALLBACKS.get(state.step, "I'm here! Go ahead whenever you're ready.")
                    if getattr(settings, "VOICE_LLM_FOR_SILENCE_NUDGES", False):
                        await _speak_llm(ws, context, fallback)
                    else:
                        await _send_text(ws, fallback)
                    continue

                # Check for off-script intents before step handlers
                if await _handle_off_script(ws, state, speech):
                    continue

                state.retries = 0
                handler = STEP_HANDLERS.get(state.step, handle_name)
                await handler(ws, state, speech)

            elif msg_type == "interrupt":
                logger.info(
                    "Voice WS [%s] caller interrupted",
                    state.call_sid[:8] if state else "?",
                )

            elif msg_type == "dtmf":
                digit = msg.get("digit", "")
                logger.info(
                    "Voice WS [%s] DTMF: %s",
                    state.call_sid[:8] if state else "?",
                    digit,
                )
                if digit == "0":
                    # Transfer to front desk
                    await _end_session(
                        ws,
                        "Absolutely, let me connect you to our front desk. One moment!",
                    )
                elif digit == "*":
                    # Repeat last response
                    last = _last_responses.get(id(ws), "")
                    if last:
                        await _send_text(ws, last)
                    else:
                        await _send_text(ws, "I'm here! Go ahead whenever you're ready.")

            elif msg_type == "error":
                logger.error(
                    "Voice WS ConversationRelay error: %s",
                    msg.get("description", "unknown"),
                )

    except WebSocketDisconnect:
        _last_responses.pop(id(ws), None)
        if state:
            logger.info("Voice WS [%s] disconnected", state.call_sid[:8])
    except Exception:
        _last_responses.pop(id(ws), None)
        logger.exception("Voice WS unexpected error")
        if state:
            await async_upsert_voice_call_log(
                call_sid=state.call_sid,
                from_number=state.from_number,
                outcome=VoiceCallLog.Outcome.OPENAI_FAILED,
                detail="WebSocket error",
            )


