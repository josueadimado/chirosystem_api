# ─── Off-script intent detection ──────────────────────────────────────

_HOURS_PATTERNS = [
    "what are your hours", "when are you open", "what time do you open",
    "what time do you close", "are you open", "hours of operation",
    "business hours", "opening hours", "when do you close",
]
_PRICE_PATTERNS = [
    "how much", "what does it cost", "what's the price", "what is the price",
    "pricing", "cost of", "price of", "how much does",
]
_CANCEL_PATTERNS = [
    "cancel", "reschedule", "change my appointment", "move my appointment",
]
_TRANSFER_PATTERNS = [
    "talk to a person", "speak to someone", "real person", "human",
    "front desk", "receptionist", "operator", "talk to someone",
    "speak to a person", "actual person", "transfer me",
]
_REPEAT_PATTERNS = [
    "repeat that", "say that again", "what did you say", "can you repeat",
    "come again", "say again", "i didn't hear", "didn't catch that",
    "one more time", "pardon",
]


async def _handle_off_script(ws: WebSocket, state: ConversationState, speech: str) -> bool:
    """Check for off-script intents. Returns True if handled (caller stays on line)."""
    lower = speech.lower().strip()

    # Repeat last response
    if any(p in lower for p in _REPEAT_PATTERNS):
        last = _last_responses.get(id(ws), "")
        if last:
            await _send_text(ws, last)
        else:
            await _send_text(ws, "Sure! Go ahead, I'm listening.")
        return True

    # Transfer to human
    if any(p in lower for p in _TRANSFER_PATTERNS):
        await _end_session_speaking_llm(
            ws,
            "The caller wants to speak to a real person. Say something warm like "
            "'Absolutely, let me connect you to the front desk.' Then end the session.",
            "Of course! Let me transfer you to our front desk right now. One moment!",
        )
        return True

    # Business hours
    if any(p in lower for p in _HOURS_PATTERNS):
        clinic = await _clinic_get_solo_async()
        hours_info = ""
        if clinic.business_hours:
            try:
                bh = json.loads(clinic.business_hours) if isinstance(clinic.business_hours, str) else clinic.business_hours
                parts = []
                for day, times in bh.items():
                    if times:
                        parts.append(f"{day}: {times}")
                hours_info = "; ".join(parts) if parts else "Monday to Friday, 9 AM to 6 PM"
            except Exception:
                hours_info = "Monday to Friday, 9 AM to 6 PM"
        else:
            hours_info = "Monday to Friday, 9 AM to 6 PM"
        await _speak_llm(
            ws,
            f"The caller asked about business hours. Answer them, then bring them back to booking. "
            f"REQUIRED FACTS — hours: {hours_info}. Current step: {state.step}.",
            f"Our hours are {hours_info}. Now, would you like to go ahead and book an appointment?",
        )
        return True

    # Pricing
    if any(p in lower for p in _PRICE_PATTERNS):
        catalog = state.catalog or (await _booking_catalog_async())
        svcs = catalog.get("services") or []
        price_list = ", ".join(f"{s['name']}: ${s['price']} ({s['duration_minutes']} min)" for s in svcs)
        await _speak_llm(
            ws,
            f"The caller asked about pricing. Tell them the prices, then guide back to booking. "
            f"REQUIRED FACTS — {price_list}.",
            f"Here are our prices: {price_list}. Would you like to book one of these?",
        )
        return True

    # Cancel / reschedule
    if any(p in lower for p in _CANCEL_PATTERNS):
        await _speak_llm(
            ws,
            "The caller wants to cancel or reschedule. Let them know they can call the front desk "
            "or visit the website for that. Offer to book a new appointment if they want.",
            "For cancellations or rescheduling, you can call our front desk directly or visit our website. "
            "Would you like to book a new appointment instead?",
        )
        return True

    return False


# ─── Slot suggestions ─────────────────────────────────────────────────

def _find_nearby_slots(
    provider_id: int | None,
    service_id: int,
    appt_date: date_type,
    rejected_time,
    max_suggestions: int = 3,
) -> list[str]:
    """Find the nearest available slots to a rejected time on the same date."""
    from apps.clinic.models import Provider, Service
    from apps.clinic.booking_availability import provider_interval_blocked_online
    from datetime import time as time_cls

    if not provider_id:
        return []
    provider = Provider.objects.filter(pk=provider_id, active=True).first()
    service = Service.objects.filter(pk=service_id, is_active=True).first()
    if not provider or not service:
        return []

    duration = service.duration_minutes
    slot_step = 15 if service.service_type == "chiropractic" else max(duration, 15)
    day_start, day_end = 9 * 60, 18 * 60

    taken = set()
    for s, e in (
        Appointment.objects.filter(provider=provider, appointment_date=appt_date)
        .exclude(status__in=[
            Appointment.Status.CANCELLED, Appointment.Status.NO_SHOW,
            Appointment.Status.COMPLETED,
        ])
        .values_list("start_time", "end_time")
    ):
        for m in range(s.hour * 60 + s.minute, e.hour * 60 + e.minute):
            taken.add(m)

    slots: list[tuple[int, str]] = []
    cursor = day_start
    target_min = rejected_time.hour * 60 + rejected_time.minute if rejected_time else 600
    while cursor + duration <= day_end:
        if not any(cursor <= t < cursor + duration for t in taken):
            h, m = divmod(cursor, 60)
            st = time_cls(hour=h, minute=m)
            et_total = cursor + duration
            eh, em = divmod(et_total, 60)
            et = time_cls(hour=min(eh, 23), minute=em if eh < 24 else 59)
            if not provider_interval_blocked_online(provider.pk, appt_date, st, et):
                suffix = "AM" if h < 12 else "PM"
                dh = h if 1 <= h <= 12 else (h - 12 if h > 12 else 12)
                label = f"{dh}:{m:02d} {suffix}"
                slots.append((abs(cursor - target_min), label))
        cursor += slot_step

    slots.sort(key=lambda x: x[0])
    return [label for _, label in slots[:max_suggestions]]


_find_nearby_slots_async = sync_to_async(_find_nearby_slots, thread_sensitive=True)


