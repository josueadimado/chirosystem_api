async def _book_single(
    ws: WebSocket, state: ConversationState, svc: ServiceEntry, phone: str
) -> tuple | None:
    try:
        appt_date = date_type.fromisoformat(svc.appointment_date)
    except (ValueError, KeyError):
        state.step = "datetime"
        state.retries = 0
        await _speak_llm(
            ws,
            "Something went wrong with the date internally. "
            "Ask the caller to say the date and time again. Sound apologetic but casual.",
            "Sorry about that — could you give me the date and time one more time?",
        )
        return None

    t = _parse_time_12h(svc.start_time)
    if not t:
        state.step = "datetime"
        state.retries = 0
        await _speak_llm(
            ws,
            "Something went wrong with the time internally. "
            "Ask the caller to repeat the date and time. Sound apologetic.",
            "Sorry, I had a hiccup with the time. Could you say it again for me?",
        )
        return None

    payload = {
        "first_name": state.first_name[:100],
        "last_name": state.last_name[:100],
        "phone": phone,
        "email": "",
        "service_id": svc.service_id,
        "service_duration_minutes": int(svc.service_duration),
        "service_price": Decimal(str(svc.service_price)),
        "appointment_date": appt_date,
        "start_time": t,
    }
    if svc.provider_id:
        payload["provider_id"] = int(svc.provider_id)

    transcript = (
        f"Name: {state.first_name} {state.last_name}, "
        f"Service: {svc.service_name}, "
        f"Date: {svc.appointment_date}, Time: {svc.start_time}"
    )

    status, appt, err_detail = await _book_single_try_async(
        payload,
        call_sid=state.call_sid,
        from_number=state.from_number,
        transcript=transcript,
    )
    if status == "serializer":
        await _offer_alternative_slots(ws, state, svc, t)
        return None
    if status == "intake":
        await _handle_chiropractic_intake_block(ws, state, svc, err_detail or "")
        return None
    if status == "slot":
        await _offer_alternative_slots(ws, state, svc, t)
        return None

    when_plain = _booking_datetime_plain_english(
        appt.appointment_date, appt.start_time, today=_today_clinic_date()
    )
    return appt, when_plain


async def _offer_alternative_slots(ws, state, svc, rejected_time):
    """When a slot is rejected, find nearby alternatives and offer them."""
    svc.appointment_date_backup = svc.appointment_date
    svc.appointment_date = ""
    svc.start_time = ""
    state.step = "datetime"
    state.retries = 0

    try:
        appt_date = date_type.fromisoformat(svc.appointment_date_backup)
        nearby = await _find_nearby_slots_async(
            svc.provider_id, svc.service_id, appt_date, rejected_time
        )
    except Exception:
        nearby = []

    if nearby:
        slot_list = ", ".join(nearby[:3])
        await _speak_llm(
            ws,
            f"The requested time for {svc.service_name} is not available. "
            f"But you found these nearby open slots on the same day: {slot_list}. "
            f"Offer them naturally. The caller can also pick a totally different date. "
            f"REQUIRED FACTS — available slots: {slot_list}",
            f"That time's taken, but I have {slot_list} available. "
            f"Would any of those work, or would you prefer a different day?",
        )
    else:
        await _speak_llm(
            ws,
            f"The time for {svc.service_name} is taken and no other slots are available on that day. "
            f"Ask the caller to try a different date. Be empathetic.",
            f"Unfortunately that slot is taken and the rest of the day looks full. "
            f"Would you like to try a different date?",
        )


