RECEPTIONIST_PERSONA = """\
You are Sarah, the front-desk receptionist at Relief Chiropractic. You love helping people feel \
welcome and you take real pride in getting their care scheduled smoothly. You're on a phone call \
with a patient or future patient.

TONE — JOVIAL YET PROFESSIONAL:
- Sound genuinely glad they called: bright, upbeat, and human — like a great front desk who \
enjoys their job. A touch of warmth and light enthusiasm is perfect.
- Stay professional for healthcare: respectful, clear, never flippant about health, pain, or \
scheduling. No slang that sounds teenage; no jokes at the patient's expense.
- Use natural contractions (you're, we'll, that's). Sprinkle in friendly phrases like \
"Wonderful!", "I'd be happy to help with that", "That's great", "Love it — let's get you booked", \
"Perfect timing" — but rotate; don't repeat the same opener every time.
- Vary your wording every reply so it never feels robotic or scripted.
- One short beat of rapport is fine (e.g. glad they chose the clinic, hope their day's going well) \
— then move the booking forward.
- Keep each reply SHORT: 1–3 sentences. Phone callers want quick, clear answers.

NEVER:
- Say you're an AI, bot, or automated system — you are Sarah at the desk.
- Invent services, prices, times, or policies not given in the instructions.

ALWAYS:
- Include every REQUIRED FACT from the instructions — names, dates, times, services, prices.
"""


async def _llm_respond(instruction: str, fallback: str) -> str:
    """Call GPT-4o-mini to generate a natural response. Falls back to static text."""
    api_key = getattr(settings, "OPENAI_API_KEY", "") or ""
    if not api_key.strip():
        return fallback

    model = getattr(settings, "OPENAI_VOICE_MODEL", None) or "gpt-5.4-nano"

    try:
        client = await _get_http_client()
        resp = await client.post(
            "https://api.openai.com/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model,
                "temperature": 0.82,
                "max_tokens": 120,
                "messages": [
                    {"role": "system", "content": RECEPTIONIST_PERSONA},
                    {"role": "user", "content": instruction},
                ],
            },
        )
        resp.raise_for_status()
        data = resp.json()
        text = data["choices"][0]["message"]["content"].strip()
        text = text.strip('"').strip("'")
        return text if text else fallback
    except Exception:
        logger.exception("LLM response generation failed, using fallback")
        return fallback


async def _stream_openai_tokens_to_ws(ws: WebSocket, instruction: str, fallback: str) -> str:
    """
    Stream chat completion deltas to Twilio as they arrive (last=false), then one final last=true.
    Twilio recommends this over a single blob for lower latency to first audio.
    """
    api_key = (getattr(settings, "OPENAI_API_KEY", "") or "").strip()
    if not api_key:
        await _send_text(ws, fallback)
        return fallback

    model = getattr(settings, "OPENAI_VOICE_MODEL", None) or "gpt-5.4-nano"
    queued: str | None = None
    full_parts: list[str] = []

    try:
        client = await _get_http_client()
        async with client.stream(
            "POST",
            "https://api.openai.com/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model,
                "temperature": 0.82,
                "max_tokens": 120,
                "stream": True,
                "messages": [
                    {"role": "system", "content": RECEPTIONIST_PERSONA},
                    {"role": "user", "content": instruction},
                ],
            },
            timeout=httpx.Timeout(45.0, connect=10.0),
        ) as resp:
            resp.raise_for_status()
            async for line in resp.aiter_lines():
                if not line.startswith("data: "):
                    continue
                data = line[6:].strip()
                if data == "[DONE]":
                    break
                try:
                    obj = json.loads(data)
                except json.JSONDecodeError:
                    continue
                choices = obj.get("choices") or []
                if not choices:
                    continue
                delta = (choices[0].get("delta") or {})
                content = delta.get("content") or ""
                if not content:
                    continue
                full_parts.append(content)
                if queued is not None:
                    await ws.send_json({"type": "text", "token": queued, "last": False})
                queued = content

        if queued is not None:
            await ws.send_json({"type": "text", "token": queued, "last": True})
        full = "".join(full_parts).strip().strip('"').strip("'")
        if not full:
            await _send_text(ws, fallback)
            return fallback
        _last_responses[id(ws)] = full
        return full
    except Exception:
        logger.exception("OpenAI streaming failed, using fallback")
        await _send_text(ws, fallback)
        return fallback


async def _speak_llm(ws: WebSocket, instruction: str, fallback: str) -> str:
    """Generate Sarah's line: stream to Twilio when VOICE_LLM_STREAM is on, else one round-trip."""
    if getattr(settings, "VOICE_LLM_STREAM", True) and (getattr(settings, "OPENAI_API_KEY", "") or "").strip():
        return await _stream_openai_tokens_to_ws(ws, instruction, fallback)
    text = await _llm_respond(instruction, fallback)
    await _send_text(ws, text)
    return text


async def _end_session_speaking_llm(ws: WebSocket, instruction: str, fallback: str):
    await _speak_llm(ws, instruction, fallback)
    await ws.send_json({"type": "end"})


# ─── Helpers ──────────────────────────────────────────────────────────

def _svc_list(catalog: dict) -> str:
    svcs = catalog.get("services") or []
    chiro = [s["name"] for s in svcs if s.get("service_type") == "chiropractic"]
    mass = [s["name"] for s in svcs if s.get("service_type") == "massage"]
    parts = []
    if chiro:
        parts.append("Chiropractic: " + ", ".join(chiro))
    if mass:
        parts.append("Massage: " + ", ".join(mass))
    return ". ".join(parts) if parts else ", ".join(s["name"] for s in svcs)


def _svc_names(services: list[dict]) -> str:
    names = [s["name"] for s in services]
    if len(names) == 1:
        return names[0]
    if len(names) == 2:
        return f"{names[0]} and {names[1]}"
    return ", ".join(names[:-1]) + f", and {names[-1]}"


# Track last response for "repeat" support
_last_responses: dict[str, str] = {}


async def _send_text(ws: WebSocket, text: str, *, last: bool = True):
    await ws.send_json({
        "type": "text",
        "token": text,
        "last": last,
    })
    # Store for repeat — keyed by ws id
    _last_responses[id(ws)] = text


async def _end_session(ws: WebSocket, text: str = ""):
    if text:
        await _send_text(ws, text, last=True)
    await ws.send_json({"type": "end"})


# ─── Service entry ────────────────────────────────────────────────────

class ServiceEntry:
    def __init__(self, svc: dict, provider_id: int | None = None, provider_name: str = ""):
        self.service_id: int = svc["id"]
        self.service_name: str = svc["name"]
        self.service_duration: int = svc["duration_minutes"]
        self.service_price: str = svc["price"]
        self.provider_id: int | None = provider_id
        self.provider_name: str = provider_name
        self.appointment_date: str = ""
        self.start_time: str = ""
        self.booked_appt = None


# ─── Conversation state ──────────────────────────────────────────────

class ConversationState:
    def __init__(self, call_sid: str, from_number: str):
        self.call_sid = call_sid
        self.from_number = from_number
        self.step = "name"
        self.retries = 0
        self.first_name = ""
        self.last_name = ""
        self.catalog: dict | None = None
        self.is_returning = False

        self.services: list[ServiceEntry] = []
        self.current_svc_idx: int = 0
        self.pending_categories: list[str] = []
        self.last_service_id: int | None = None  # from appointment history

    @property
    def current_service(self) -> ServiceEntry | None:
        if 0 <= self.current_svc_idx < len(self.services):
            return self.services[self.current_svc_idx]
        return None

    @property
    def is_multi(self) -> bool:
        return len(self.services) > 1

    @property
    def has_more_services(self) -> bool:
        return self.current_svc_idx < len(self.services) - 1

    @property
    def caller_name(self) -> str:
        return f"{self.first_name} {self.last_name}".strip()


# ─── Patient lookup ──────────────────────────────────────────────────

def _lookup_patient(from_number: str) -> Patient | None:
    if not from_number:
        return None
    normalized = normalize_phone(from_number)
    if not normalized:
        return None
    try:
        return Patient.objects.get(phone=normalized)
    except Patient.DoesNotExist:
        return None
    except Patient.MultipleObjectsReturned:
        return Patient.objects.filter(phone=normalized).first()


def _returning_patient_voice_setup(from_number: str) -> dict | None:
    """Load returning-patient context from DB (sync only). Returns None if unknown caller."""
    patient = _lookup_patient(from_number)
    if not patient:
        return None
    catalog = _booking_catalog_json()
    last_sid = None
    last_appt = (
        Appointment.objects.filter(patient=patient)
        .exclude(
            status__in=[
                Appointment.Status.CANCELLED,
                Appointment.Status.NO_SHOW,
            ],
        )
        .order_by("-appointment_date")
        .first()
    )
    if last_appt and last_appt.booked_service_id:
        last_sid = last_appt.booked_service_id
    return {
        "first_name": patient.first_name,
        "last_name": patient.last_name,
        "catalog": catalog,
        "last_service_id": last_sid,
    }


