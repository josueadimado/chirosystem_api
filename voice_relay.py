# Compound phrases checked first — order matters
_NO_PHRASES = [
    "not right", "not correct", "that's wrong", "thats wrong",
    "no way", "don't think so", "i don't think so",
    "i'd rather not", "not really", "no thanks", "no thank you",
    "start over", "never mind", "nevermind",
]
_YES_PHRASES = [
    "go ahead", "book it", "sounds good", "yes please", "that works",
    "that's right", "thats right", "that's correct", "thats correct",
    "let's do it", "do it", "for sure",
]

_YES_WORD_SET = {
    "yes", "yeah", "yep", "yup", "correct", "right", "sure", "ok", "okay",
    "confirm", "please", "absolutely", "perfect", "yea", "affirmative",
}
_NO_WORD_SET = {
    "no", "nope", "nah", "wrong", "cancel", "incorrect", "change",
}


def _detect_yes_no(speech: str) -> tuple[bool, bool]:
    """Return (is_yes, is_no) using phrase priority then word-boundary matching."""
    lower = speech.lower().strip()

    # Phrase matching takes priority
    for phrase in _NO_PHRASES:
        if phrase in lower:
            return False, True
    for phrase in _YES_PHRASES:
        if phrase in lower:
            return True, False

    # Word-boundary matching (avoids "right" inside "not right", etc.)
    words = set(_re.findall(r"\b\w+(?:'\w+)?\b", lower))
    is_yes = bool(words & _YES_WORD_SET)
    is_no = bool(words & _NO_WORD_SET)
    return is_yes, is_no


